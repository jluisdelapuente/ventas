                    <h6 class="inner">Conceptos b&aacute;sicos en Archiv&iacute;stica</h6>
                    <div class="blog-posted-row">&nbsp;&nbsp; Procesos T&eacute;cnicos Archiv&iacute;sticos</div>
                    <div>
Son: Administraci&oacute;n, Organizaci&oacute;n, Selecci&oacute;n, Descripci&oacute;n, Conservaci&oacute;n y Servicio. </P>

<P><b>Administraci&oacute;n de archivos.- </b>Es un conjunto de principios, m&eacute;todos y procedimientos orientados a lograr una eficiente organizaci&oacute;n y funcionamiento de los Archivos. Es funci&oacute;n archiv&iacute;stica la gesti&oacute;n de documentos desde su elaboraci&oacute;n hasta la eliminaci&oacute;n o conservaci&oacute;n permanente de acuerdo a su ciclo vital. </P>

<P><b>Organizaci&oacute;n documental.- </b>Junto con el organismo nacen los archivos. "Donde quiera que comenz&oacute; a gestarse una organizaci&oacute;n administrativa, &eacute;sta trajo consigo la creaci&oacute;n de documentos y el archivo los recogi&oacute;".</P>

<P>La organizaci&oacute;n de documentos archiv&iacute;sticos, consiste en el desarrollo de un conjunto de acciones o procedimientos orientados a la clasificaci&oacute;n, la ordenaci&oacute;n y la signatura o codificaci&oacute;n de los documentos, teniendo en cuenta los principios de procedencia y de orden original. 
Es la forma como se disponen los documentos para su localizaci&oacute;n y recuperaci&oacute;n inmediata de la informaci&oacute;n. </P>

<P>La Organizaci&oacute;n Documental comprende: La Clasificaci&oacute;n, el Ordenamiento y la Signatura
En la clasificaci&oacute;n se identificar&aacute;n las series documentales y se establecer&aacute;n criterios uniformes. 
Enti&eacute;ndase por serie documental al conjunto de documentos que tienen caracter&iacute;sticas comunes; el mismo tipo documental o el mismo asunto y que por consiguiente son archivados, usados, transferidos o eliminados como unidad. </P>

<P>El ordenamiento de las series documentales se efectuar&aacute; aplicando el sistema m&aacute;s conveniente para la instituci&oacute;n (num&eacute;rico, cronol&oacute;gico, alfab&eacute;tico o una combinaci&oacute;n de ellos). </P>

<P><b>Signaci&oacute;n.- </b>Signar un documento es un procedimiento en la que se determina un CODIGO que identifique en forma precisa y breve las secciones (unidades org&aacute;nicas) y las series documentales. </P>

<P>La Signatura consiste en asignar s&iacute;mbolos particulares a los documentos para su identificaci&oacute;n. Dichos s&iacute;mbolos reemplazan a los nombres o t&iacute;tulos, represent&aacute;ndolos, lo que permite su inmediata localizaci&oacute;n. </P>

<P>
El c&oacute;digo se estructura en base a letras, n&uacute;meros o la combinaci&oacute;n de ambos. De esta manera se tiene una identificaci&oacute;n alfab&eacute;tica, num&eacute;rica o alfa-num&eacute;rica</P>

<P><b>Selecci&oacute;n documental.- </b>Es un proceso archiv&iacute;stico que consiste en identificar, analizar y evaluar todas las series documentales que genera y recibe una entidad para determinar sus per&iacute;odos de retenci&oacute;n, en base a los cuales se formular&aacute; el Programa de Control de Documentos</P>

<P><b>Descripci&oacute;n documental.- </b>Es un proceso que consiste en elaborar instrumentos auxiliares descriptivos que permitan conocer, localizar y controlar las series documentales. </P>

<P>El inventario, es una herramienta descriptiva necesaria en toda transferencia documental donde se registran las series documentales producidas y recibidas por cada unidad org&aacute;nica en cumplimiento de sus funciones. </P>

<P><b>Conservaci&oacute;n documental.- </b> Mantenimiento del soporte y texto, mediante medidas de preservaci&oacute;n y restauraci&oacute;n de los documentos de archivo. </P>

<P><b>Servicio.- </b> Pr&eacute;stamo, consulta, b&uacute;squeda, exhibici&oacute;n, reproducci&oacute;n del acervo documental de una instituci&oacute;n. </P>

<P><b>Fondo documental.- </b>Conjunto total de documentos o archivos reunidos por una instituci&oacute;n en el ejercicio de sus actividades y funciones. </P>
<P><b>Secci&oacute;n documental.- </b>Conjunto de documentos procedente de una unidad org&aacute;nica o divisi&oacute;n administrativa importante, que tiene una estructura, funciones y fines conocidos y su cumplimiento determina las series documentales. </P>
<P><b>Serie documental.- </b>Est&aacute; constituida por los documentos producidos o recibidos de una oficina o unidad org&aacute;nica en cumplimiento en sus funciones, que tienen el mismo tipo documental o se refieren a un mismo asunto, y que son archivados, conservados, utilizados, valorados, transferidos o eliminados como unidad. </P>
<P><b>Principio de procedencia.- </b>Establece que en la Organizaci&oacute;n Documental los documentos producidos por una instituci&oacute;n u organismo no deben mezclarse con los de otra. </P>
<P><b>Principio de orden original.- </b>Establece que en una Organizaci&oacute;n Documental no se debe modificar la organizaci&oacute;n transferida a un fondo, por una unidad productora en el nivel de archivo correspondiente. </P>
<P><b>Documentos de valor temporal.- </b> Por su contenido dejan de ser imprescindibles y sin trascendencia. Carecen de valor al cumplirse su fin administrativo, fiscal, contable, legal, administrativo y otros que los origin&oacute;. </P>
<P><b>Documentos de valor permanente.- </b> Indispensables para la entidad de origen o para otras entidades. </P>
<P>Ejemplos: </P>
<ul>
	<li>Referentes a la protecci&oacute;n de los derechos ciudadanos.
	<li>Los que reflejan la evoluci&oacute;n de la entidad(atribuciones, funciones, estructura, procedimientos...).
	<li>Los que aportan al estudio y la investigaci&oacute;n.
</ul>
<P><b>Cuadro de clasificaci&oacute;n de documentos.- </b>Es un esquema donde se encuentran estructuradas las series documentales de cada unidad org&aacute;nica que conforman las secciones de un fondo documental. 
Es el resultado de relacionar las clases (series y secciones) entre s&iacute; y su ubicaci&oacute;n dentro del contexto general.
El Cuadro de Clasificaci&oacute;n, se elabora por cada unidad org&aacute;nica (secci&oacute;n). Sirve para: </P>
<ul>
	<li>Determinar las funciones y actividades de la unidad org&aacute;nica.
	<li>Establecer un esquema de acuerdo a la estructura org&aacute;nica.
	<li>Determinar las series priorizando el asunto que trata.
	<li>Codificar la secci&oacute;n y la serie en forma correlativa
</ul>
<P><b>Programa de Control de Documentos (PCD) .- </b>Es un documento de gesti&oacute;n archiv&iacute;stica que establece las series documentales que produce y recibe una entidad p&uacute;blica precisando el n&uacute;mero de a&ntilde;os que deben conservarse y los per&iacute;odos de retenci&oacute;n en cada nivel de archivo. </P>




                     
